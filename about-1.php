            <h1>Услуги доставки</h1>
            <div class="content-form-page">
              <div class="row">
                <div class="col-md-8 col-sm-8">
На сайте www.postscanner.ru каждый клиент может ознакомиться с информацией, необходимой при выборе курьерской службы. 
<br />
<br />
Выбирая для себя курьерскую службу, просим Вас обратить внимание на ряд нюансов:
<br />
<br />
<h4>Необходимость использования дополнительной упаковки при отправке хрупкого или особо ценного груза</h4>
Упаковка бывает самой разной. Большинство курьерских служб сами предлагают клиентам упаковать их груз. Для данной услуги обычно предусмотрена дополнительная оплаты, хотя многие компании оказывают ее бесплатно. Профессионально выполненная упаковка любых грузов или документов обеспечит их защиту и абсолютную сохранность при пересылке, избавив Вас от возможных переживаний. 
<br />
<br />
<h4>Страхование груза</h4> 
Курьерские службы предлагают клиентам застраховать важные для них или особо ценные грузы или документы. Компании оказывают эти услуги по собственной программе страхования, которая учитывает самые различные факторы. Иногда страховка уже включена в стоимость услуг. Часть компаний может предложить клиенту несколько различных программ страхования на выбор, предоставляя возможность самостоятельно сделать оптимальный выбор. 
<br />
<br />
<h4>«Доставка груза ко времени»</h4> 
Эта услуга позволяет клиенту обеспечить доставку груза адресату в обозначенное время. Ее Преимущества - оперативность и высокий уровень ответственности при доставки. Обычно курьерские службы доставляют груз в течении 24-х часов, в зависимости от маршрута доставки. Существует весовое ограничении - 15 кг. Негабаритный груз и отдаленный регион становятся реальной проблемой при доставке. 
<br />
<br />
<h4>«Доставка груза лично в руки»</h4>
Данная услуга позволяет клиенту передать необходимые документы или груз строго определенному человеку только после предъявления паспорта. Такую услугу оказывают не все курьерские службы, поскольку в нашей стране она только развивается. Ей можно воспользоваться исключительно в крупных компаниях. Преимуществами использования этой услуги является строгая конфиденциальность, гарантирующая, что пересылаемый груз или документы не получит никто посторонний. «Доставку груза лично в руки» могут заказать частные лица или организации. При заказе данной услуги клиенту следует знать точные реквизиты адресата. Также необходимо сообщать номер мобильного телефона, поскольку представитель курьерской службы предварительно связывается с получателем и договаривается об удобном времени доставки. Груз или документы передаются только при предъявлении получателем документа, подтверждающего его личность (паспорт). 
<br />
<br />
<h4>«Доставка груза в день заказа»</h4>
Экспресс доставка осуществляется в течение рабочего дня. Данный вид услуг широко развит в пределах одного города или небольшого региона. Большинство курьерских компаний осуществляет реализацию данной услуги в течение 4-7 часов после размещения заказа. Такая услуга также выполняется непосредственно в руки получателя. Заказ по данной услуге обычно принимается до 14:00. Большинство курьерских компаний в рамках такой услуги осуществляют доставку грузов стандартных габаритов и размеров. 
<br />
<br />
<h4>«Доставка груза от двери до двери»</h4> 
Курьерская служба самостоятельно выедет к клиенту, заберет у него необходимые для пересылки груз или документы и доставит их непосредственному получателю. Клиент существенно экономит собственное время, возлагая решение всех проблемы на курьерские службы. Преимуществами такой услуги являются оперативность доставки за счет исключения различных промежуточных звеньев и ее безопасность, которая полностью возлагается на курьерскую службу. В процессе реализации данной услуги компании тщательно прорабатывают маршрут доставки и выбирают оптимальный транспорт. 
<br />
<br />
<h4>SMS информирование</h4>
Клиент сможет контролировать процесс прохождения его грузом всех важнейших этапов доставки, включая передачу непосредственному получателю. Такая услуга предоставляется большинством курьерских компаний, поскольку позволяет клиенту удобно и оперативно получать интересующие сведения. 
<br />
<br />
<h4>Выдача идентификационного номера</h4> 
Большинство курьерских компаний присваивают по желанию клиента его грузу идентификационный номер, по которому он сможет отследить процесс доставки. Отслеживание осуществляется на порталах в сети интернет самой курьерской службы, а также на некоторых независимых сайтах.

